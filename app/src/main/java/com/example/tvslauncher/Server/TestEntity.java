package com.example.tvslauncher.Server;

public class TestEntity {
    private String sideStand;
    private String speed;
    private String leftIndicator;
    private String rightIndicator;
    private String headlight;
    private String dipper;

    public TestEntity() {
    }

    public String getSideStand() {
        return sideStand;
    }

    public void setSideStand(String sideStand) {
        this.sideStand = sideStand;
    }

    public String getSpeed() {
        return speed;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }

    public String getLeftIndicator() {
        return leftIndicator;
    }

    public void setLeftIndicator(String leftIndicator) {
        this.leftIndicator = leftIndicator;
    }

    public String getRightIndicator() {
        return rightIndicator;
    }

    public void setRightIndicator(String rightIndicator) {
        this.rightIndicator = rightIndicator;
    }

    public String getHeadlight() {
        return headlight;
    }

    public void setHeadlight(String headlight) {
        this.headlight = headlight;
    }

    public String getDipper() {
        return dipper;
    }

    public void setDipper(String dipper) {
        this.dipper = dipper;
    }
}
